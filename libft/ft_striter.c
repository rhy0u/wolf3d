#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int		i;

	if (!s | !f)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		f(&s[i]);
		i++;
	}
}
